package prog;

public class Relationaloperators {

	public static void main(String[] args) {
		// Relational operators
		int num1=10, num2=20;
		System.out.println("num1 < num2 -- "+(num1 < num2));
		System.out.println("num1 > num2 -- "+(num1 > num2));
		System.out.println("num1 <= num2 -- "+(num1 <= num2));
		System.out.println("num1 >= num2 -- "+(num1 >= num2));
		System.out.println("num1 == num2 -- "+(num1 == num2));
		System.out.println("num1 != num2 -- "+(num1 != num2));

	}

}
